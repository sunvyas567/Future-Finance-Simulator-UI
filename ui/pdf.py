from playwright.async_api import async_playwright

async def generate_pdf(html_file, output_pdf):
    #print("DEBUG: Starting PDF generation with Playwright...")
    async with async_playwright() as p:
        browser = await p.chromium.launch()
        page = await browser.new_page()

        # Load file
        #-await page.goto(f"file://{html_file}", wait_until="networkidle")
        await page.set_content(html_file, wait_until="networkidle")


        # Render like browser (NOT print)
        await page.emulate_media(media="screen")

        # Wait for charts / JS
        await page.wait_for_timeout(2000)

        # Fix pagination + clipping
        await page.add_style_tag(content="""
        * {
            page-break-inside: avoid !important;
            break-inside: avoid !important;
        }

        .chart-container, canvas, .plotly {
            overflow: visible !important;
            height: auto !important;
            min-height: 400px !important;
        }
        """)

        # Generate PDF
        pdf_bytes = await page.pdf(
            format="A4",
            print_background=True,
            prefer_css_page_size=True,
            display_header_footer=True,
            margin={"top":"15mm","bottom":"15mm","left":"10mm","right":"10mm"},
            header_template="""
            <div style="
                width:100%;
                font-size:20px;
                padding:0 20px;
                color:#6b7280;
                text-align:center;
                border-bottom:1px solid #e5e7eb;
            ">
                Retirement Financial Summary · Future Finance Simulator
            </div>
            """,
            footer_template="""
            <div style="
                width:100%;
                font-size:15px;
                padding:0 20px;
                color:#9ca3af;
                text-align:right;
            ">
                Page <span class="pageNumber"></span> of <span class="totalPages"></span>
            </div>
            """
        )
        
        await browser.close()
        #print(f"DEBUG: PDF generated and saved to {output_pdf}")
        return pdf_bytes

from playwright.sync_api import sync_playwright

def _html_to_pdf_bytes(html: str) -> bytes:
    #print("DEBUG: In html_to_pdf_bytes...")
    with sync_playwright() as p:
        browser = p.chromium.launch()
        page = browser.new_page()

        # Load HTML
        page.set_content(html, wait_until="networkidle")

        # -----------------------------
        # WAIT FOR PLOTLY FULL RENDER
        # -----------------------------
        page.wait_for_function("""
            () => {
                if (!window.Plotly) return false;

                const charts = document.querySelectorAll('.plotly-graph-div');
                if (charts.length === 0) return true;

                return Array.from(charts).every(
                    c => c._fullLayout && c._fullLayout._plots
                );
            }
        """)

        # -----------------------------
        # WAIT UNTIL PAGE HEIGHT STOPS CHANGING
        # -----------------------------
        #print("DEBUG: Waiting for page height to stabilize...")
        page.wait_for_function("""
            () => {
                return new Promise(resolve => {
                    let last = document.body.scrollHeight;
                    let stable = 0;

                    const interval = setInterval(() => {
                        const now = document.body.scrollHeight;

                        if (now === last) {
                            stable++;
                            if (stable >= 3) {
                                clearInterval(interval);
                                resolve(true);
                            }
                        } else {
                            stable = 0;
                            last = now;
                        }
                    }, 300);
                });
            }
        """)

        # -----------------------------
        # FORCE FINAL LAYOUT
        # -----------------------------
        page.evaluate("""
            window.dispatchEvent(new Event('resize'));
        """)

        page.wait_for_timeout(800)

        # -----------------------------
        # GENERATE PDF
        # -----------------------------
        pdf_bytes = page.pdf(
            format="A4",
            print_background=True,
            prefer_css_page_size=True,
            display_header_footer=True,
            margin={
                "top": "20mm",
                "bottom": "20mm",
                "left": "15mm",
                "right": "15mm",
            },

            header_template="""
            <div style="
                width:100%;
                font-size:20px;
                padding:0 20px;
                color:#6b7280;
                text-align:center;
                border-bottom:1px solid #e5e7eb;
            ">
                Retirement Financial Summary · Future Finance Simulator
            </div>
            """,

            footer_template="""
            <div style="
                width:100%;
                font-size:15px;
                padding:0 20px;
                color:#9ca3af;
                text-align:right;
            ">
                Page <span class="pageNumber"></span> of <span class="totalPages"></span>
            </div>
            """
        )
      
        browser.close()
        return pdf_bytes
